    public enum ECameraMode {

        /**
         * <summary>
         *  The default camera mode.
         * </summary>
         */
        Default,

        /**
         * <summary>
         *  The first person view camera mode.
         * </summary>
         */
        FirstPersonView,

        /**
         * <summary>
         *  The front view camera mode.
         * </summary>
         */
        FrontView,

        /**
         * <summary>
         *  The third person view camera mode.
         * </summary>
         */
        ThirdPersonView,

        /**
         * <summary>
         *  No camera mode.
         * </summary>
         */
        None
    }
}