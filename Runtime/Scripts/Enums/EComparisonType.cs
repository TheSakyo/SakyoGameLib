namespace SakyoGame.Lib.Enums {

    /**
     * <summary>
     *  Enum for comparison types.
     * </summary>
     */
    public enum EComparisonType {

        /**
         * <summary>
         *  The comparison type for checking if a value is greater than another.
         * </summary>
         */
        Equal,

        /**
         * <summary>
         *  The comparison type for checking if a value is not equal to another.
         * </summary>
         */
        NotEqual,

        /**
         * <summary>
         *  The comparison type for checking if a value is greater than another.
         * </summary>
         */
        Greater,

        /**
         * <summary>
         *  The comparison type for checking if a value is greater than or equal to another.
         * </summary>
         */
        GreaterOrEqual,

        /**
         * <summary>
         *  The comparison type for checking if a value is less than another.
         * </summary>
         */
        Less,

        /**
         * <summary>
         *  The comparison type for checking if a value is less than or equal to another.
         * </summary>
         */
        LessOrEqual
    }
}